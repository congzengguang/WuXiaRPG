public class GameTimer {
    /**
     * #encoding:utf-8
     * #==============================================================================
     * # ■ Game_Timer
     * #------------------------------------------------------------------------------
     * # 　计时器。本类的实例请参考 $game_timer 。
     * #==============================================================================
     * <p>
     * class Game_Timer
     * #--------------------------------------------------------------------------
     * # ● 初始化对象
     * #--------------------------------------------------------------------------
     * def initialize
     *
     * @count = 0
     * @working = false
     * end
     */
    int count;
    boolean working;

    public void init() {
        count = 0;
        working = false;
    }
    /**
     #--------------------------------------------------------------------------
     # ● 更新画面
     #--------------------------------------------------------------------------
     def update
     if @working && @count > 0
     @count -= 1
     on_expire if @count == 0
     end
     end
     */
     public void update(){
     if(working && count>0){count -= 1;}
     }
     /**
     #--------------------------------------------------------------------------
     # ● 开始
     #--------------------------------------------------------------------------
     def start(count)
     @count = count
     @working = true
     end
     */
     public void start(int count){
     this.count = count;
     working = true;
     }
     /**
     #--------------------------------------------------------------------------
     # ● 停止
     #--------------------------------------------------------------------------
     def stop
     @working = false
     end
     */
     public void stop(){working = false;}
 /**
     #--------------------------------------------------------------------------
     # ● 判定是否正在工作
     #--------------------------------------------------------------------------
     def working?
     @working end
     */
     public boolean isWorking(){
     return working;
     }
     /**
     #--------------------------------------------------------------------------
     # ● 获取秒数
     #--------------------------------------------------------------------------
     def sec
     @count / Graphics.frame_rate
     end
     public int getSec(){
     this.count/frameRate;
     }
     #--------------------------------------------------------------------------
     # ● 计时器为 0 时的处理
     #--------------------------------------------------------------------------
     def on_expire
     BattleManager.abort
     end
     end

     */
}
